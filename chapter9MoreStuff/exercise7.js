// What is exception handling and what problem does it solve?

// Exception handling uses the try...catch statement to perform some action whenever
// an error is returned by another expression. For example, if an error is thrown
// in the try statement, the catch statement will perform some operation and
// continue the program, as opposed to halt the program and output an error.
// This allows developers to customize the behavior of a program in response to an
// error, and also allows them to continue running a program despite an error
// being thrown if desired.
